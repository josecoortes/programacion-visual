package Tema4;

public class T4PruebasSentenciaSeleccion {
    public static void main(String[] args) {
        int valor = 50;

        //Sentencia de selección simple
        if (valor >= 100) {
            System.out.println("El valor es igual o mayor que 100");
        } else{
            System.out.println("El valor es menor que 100");
        }
        System.out.println("Fin de ejecución");
    }
}
